// Head

const int N = 1123456;
const LL INF = LL(1e15);

LL f[N], g[N], a[N];
int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);

    cin >> n;
    for (int i = 1; i <= n; i++) cin >> a[i];
    for (int i = 1; i <= n; i++) {
        f[i] = max(a[i], a[i] + f[i - 1]);
    }
    for (int i = n; i >= 1; i--) {
        g[i] = max(a[i], a[i] + g[i + 1]);
    }

    LL maxm = f[1], ans = -INF;
    for (int i = 2; i <= n; i++) {
        ans = max(ans, maxm + g[i]);
        maxm = max(maxm, f[i]);
    }
    cout << ans << '\n';
    return 0;
}
