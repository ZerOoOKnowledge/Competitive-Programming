char f[] = { 'A', 'C', 'T', 'G' };

int main() {
    cin >> n >> s;
    int ans = 10000000;
    for (int i = 0; i <= n - 4; i++) {
        int cur = 0;
        for (int j = 0; j < 4; j++) {
            cur += min(abs(s[i + j] - f[j]), 26 - abs(s[i + j] - f[j]));
        }
        ans = min(ans, cur);
    }
    cout << ans << '\n';
    return 0;
}
